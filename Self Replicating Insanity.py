basename_no_ext = os.path.splitext(basename)[0]  # 'file.py' -> 'file'
basename_ext = os.path.splitext(basename)[1]  # 'file.py' -> '.py'

identifier1 = f'{basename_no_ext}-{project_title}-'
identifier2 = basename_ext  # '.py'

